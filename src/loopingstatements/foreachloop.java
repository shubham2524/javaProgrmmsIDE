package loopingstatements;

public class foreachloop {

	public static void main(String[] args) {
		
		int [] a = {100,200,300,400,500};
		for(int b :a) {
			System.out.println(b);	
		}
		
		String [] s = {"shubham","mohit","mukund"};
		for(String n :s) {
			System.out.println(n);
		}

	}

}
