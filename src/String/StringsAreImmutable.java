package String;

public class StringsAreImmutable {
	
	//String are immutable in nature
	//it mean will never change or delete 
	

	public static void main(String[] args) {
		String s1 = "abc";
		String s2 = "def";
		System.out.println(s1.concat(s2));
		System.out.println(s1);//s1
		//here you can see that value of s1 is not changed 
		
		
		
	}

}
