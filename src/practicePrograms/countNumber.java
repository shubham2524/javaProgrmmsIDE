package practicePrograms;

public class countNumber {

	public static void main(String[] args) {
		//WAP count total number divisible by 5 from 1 to 100
		int count =0;
		for(int i =1;i<=100;i++) {
			if (i%5==0) {
				count++;
			}
			
			
		}
		System.out.println(count);
		

	}

}
