package String;
//program to count no of char in a given string
public class countNoOfCharInString {

	public static void main(String[] args) {
		
		String s = "heUHKJWQL";
		
		int count =0 ;
		for(int i = 0;i<s.length();i++) {
			s.charAt(i);
			count++;
		}
		System.out.println(count);

	}

}
